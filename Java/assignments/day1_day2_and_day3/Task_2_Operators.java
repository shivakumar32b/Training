package com.assignments.day1_day2_and_day3;

public class Task_2_Operators {
	public static void main(String[] args) {
		
		int firstNum = Integer.parseInt(args[0]);
		int secondNum = Integer.parseInt(args[1]);
		
		System.out.println("Sum of two numbers is:"+(firstNum + secondNum));
		System.out.println("Subtracting second number from first number:"+(firstNum - secondNum));
		System.out.println("Multiplying two numbers:"+(firstNum * secondNum));
		System.out.println("Dividing second number from first number:"+(firstNum + secondNum));
	}
}
